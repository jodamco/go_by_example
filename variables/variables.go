package main

import "fmt"

func main() {

	var a = "initial"
	fmt.Println(a)

	var b, c int = 1, 2
	fmt.Println(b, c)

	var d = true
	fmt.Println(d)

	var e int
	fmt.Println(e)

	var strin string
	fmt.Println(strin)

	var doub float64
	fmt.Println(doub)

	var boo bool
	fmt.Println(boo)

	f := "apple"
	fmt.Println(f)
}
